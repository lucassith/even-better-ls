# Formats arguments into an LS_COLORS-complete escape sequence.
def color_char(char, clr1, clr2 = []):
  ch_str = get_unicode(char)
  if extra_space:
    ch_str = ch_str + " "
  f1, b1, f2, b2 = -1, -1, -1, -1
  o1, o2 = "", "";
  if len(clr1) == 3:
    f1, b1, o1 = clr1
  else:
    f1, b1 = clr1
  if clr2 and len(clr2) == 3:
    f2, b2, o2 = clr2
  elif clr2:
    f2, b2 = clr2

  if no_icons: # No icon mode
    if clr2:
      # If text color specified, use only that
      return "m%s\x1b" % ("%s " % (color_seq(f2, b2, str(o2))))
    else:
      return "m%s\x1b" % ("%s " % (color_seq(f1, b1, str(o1))))
  elif clr2:
    return "m%s\x1b" % ("%s%s \x1b[0m%s" % (color_seq(f1, b1, str(o1)), ch_str, color_seq(f2, b2, str(o2))))
  else:
    return "m%s\x1b" % ("%s%s " % (color_seq(f1, b1, str(o1)), ch_str))

# Formats fg and bg into an escape sequence.
def color_seq(f, b, other = ""):
  if isinstance(f, str):
    f = color_output_rgb(f)
  else:
    f = color_output(f)
  if isinstance(b, str):
    b = color_output_rgb(b, True)
  else:
    b = color_output(b, True)
  if (f != "" and b != ""):
    f += ";"
  other = list(other)
  other = ";".join(other)
  if (other != "" and (f != "" or b != "")):
    other += ";"
  return "\x1b[%s%s%sm" % (other, f, b)

# Formats a 256-color code into a fg or bg value
def color_output(c, bg = False):
  if (c == -1):
    return ""
  if (bg):
    return "48;5;%i" % c
  else:
    return "38;5;%i" % c

# Formats a hex color into a fg or bg value
def color_output_rgb(hex, bg = False):
  r, g, b = hex_to_rgb(hex)
  if (bg):
    return "48;2;%i;%i;%i" % (r, g, b)
  else:
    return "38;2;%i;%i;%i" % (r, g, b)

# Convert hex color to rgb values
def hex_to_rgb(hex):
  hex = hex.lstrip("#")
  return tuple(int(hex[i:i+2], 16) for i in (0, 2, 4))

# Return a unicode character. Python 2 and 3 complete.
def get_unicode(ch):
  try:
    return unichr(ch)
  except:
    return chr(ch)

# Turn supplied color list into proper color codes
def translate(color_list):
  for k, v in color_list.items():
    if type(v) is not str:
      if type(v[0]) is str:
        v[0] = ord(v[0])
      if len(v) is  3:
        color = color_char(v[0], v[1], v[2])
      else:
        color = color_char(v[0], v[1])
      color_list[k] = color
  return color_list

# Generate hash for cache file name
def get_hash(f_path, o_path, extra):
  return hashlib.md5((f_path + o_path + extra).encode()).hexdigest()

# Get cached generated color output if available and newer than the source files
def get_cache(c_hash, f_path, o_path, c_dir):
  f_time = os.path.getmtime(f_path)
  if o_path and os.path.isfile(o_path):
    o_time = os.path.getmtime(o_path)
    if o_time > f_time:
      f_time = o_time
  if os.path.isfile(c_dir + c_hash) and f_time < os.path.getmtime(c_dir + c_hash):
    try:
      f = open(c_dir + c_hash, "r")
      return f.read()
    except:
      return False
  else:
    return False

# Write output to cache file
def set_cache(output, c_dir, c_hash):
  if not os.path.exists(c_dir):
    os.makedirs(c_dir)
  if os.path.exists(c_dir + c_hash):
    os.remove(c_dir + c_hash)
  f = open(c_dir + c_hash, "w+")
  f.write(output)


